DROP DATABASE IF EXISTS db_films;
CREATE DATABASE db_films;

-- Utilisation de la base de données
USE db_films;

-- Création de la table Films
CREATE TABLE Films (
    FilmID INT PRIMARY KEY,
    Titre VARCHAR(100),
    AnneeSortie INT,
    Genre VARCHAR(50),
    Realisateur VARCHAR(100)
);

-- Création de la table Acteurs
CREATE TABLE Acteurs (
    ActeurID INT PRIMARY KEY,
    Nom VARCHAR(100),
    DateNaissance DATE,
    Nationalite VARCHAR(50)
);

-- Création de la table Casting pour représenter la relation entre Films et Acteurs
CREATE TABLE Casting (
    FilmID INT,
    ActeurID INT,
    Personnage VARCHAR(100),
    PRIMARY KEY (FilmID, ActeurID),
    FOREIGN KEY (FilmID) REFERENCES Films(FilmID),
    FOREIGN KEY (ActeurID) REFERENCES Acteurs(ActeurID)
);

